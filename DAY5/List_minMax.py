# 리스트의 값 중 최대값 / 최소값 반환

scores = [90, 78, 81, 64, 99]
print('최대 : ', max(scores))
print('최소 : ', min(scores))


chr = ['b', 'A', 'e', 'C']
print('최대 : ', max(chr))
print('최소 : ', min(chr))


x = ['apple', 'banana', 'Coconut', 'melon']
print('정렬 : ', sorted(x))
print('최대 : ', max(x))
print('최소 : ', min(x))


