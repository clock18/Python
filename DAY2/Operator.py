# Operator = 연산자
# minutes = seconds // 60
# 나누기 //
# 나머지 %

# 예제 1
num = 5000 // 120
rest = 5000%120
print('사탕의 개수 : %d개'%num)
print('나머지 돈 : %d원'%rest)
# / 를 쓰면 소숫점자리가 모두 표현됨.
# // 를 쓰면 소숫점 이하 숫자들을 모두 없앰.

# **은 지수를 의미
x=3
y = x**3 + 3 * x**2 + 7*x + 10
print(y)

