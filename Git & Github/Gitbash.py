# CLI : Command Line Interface
# 윈도우는 끝에 ~ 표시, 멕은 끝에 < 표시가 있음
# ~ = 홈폴더 라고 부름
# 홈폴더는 기본적으로 'user' 폴더로 지정됨

# ls = list segment ( 그 폴더 안의 내용물 리스트를 보여준다 )
# ls -a : -a를 붙이면 숨김파일 확인가능

# desktop = '바탕화면' 폴더
# date = 현재의 시간을 확인해준다

# . => 현재 위치
# .. => 상위 위치

# cd => change directory
# cd .. => 상위위치로 이동하는 명령어
# cd Desktop => Desktop 폴더로 이동 ( ls 를 쳐서 리스트 확인 후 이동 )
# cd Des 상태에서 tap을 누르면 명령어를 완성시켜줌
# cd 만 치면 홈폴더로 바로 이동

# clear를 치면 위로 쫙 밀어줌. ( 하지만 내용은 그대로 남아있음. 보이지 않을 뿐 )

# touch => '파일'을 만듬
# ex. touch a.txt => a라는 이름의 text파일을 만든다

# mkdir => '폴더'를 만듬 ( = make directory )

# start a.txt => a파일을 연다 ( 맥은 open 명령어 )

# 윗화살표를 누르면 바로전에 기입했던 명령어를 불러옴

# 절대경로 vs 상대경로
# 상대경로 => 내가 있는 위치에서!
# 절대경로 => 절대 바뀌지 않음

# rm => 파일을 지울 때는 rm a.txt
#    => 폴더를 지울 때는 rm -r test
# rm *.txt => txt확장자를 가지고 있는 모든 파일을 지움
# rm -rf * => 이 폴더안에 있는 모든 파일을 싹 다 지움.  f = force ( 강제로 )
# * = 에스터레스크 ( git에서는 all을 의미 )

# code . =>  입력하면 visual 실행됨
