# 파일 입출력

# 표준 입출력 장치 : input(), print()
# 파일 입출력 장치 : read(), write()

# 읽기용 : 변수명 = open('파일명','r')
# 쓰기용 : 변수명 = open('파일명','w')

# r : 읽기모드
# w : 쓰기모드, 기존에 파일이 있으면 덮어쓴다
# r+ : 읽기/쓰기 겸용 모드
# a : 기존에 파일이 있으면 이어서 쓴다, append의 약자
# t : 덱스트모드, 텍스트파일을 처리한다
# b : 이진모드, 이진 파일을 처리한다

# 변수명.close() : 파일 닫기